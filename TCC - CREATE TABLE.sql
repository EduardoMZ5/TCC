IF NOT EXISTS ( SELECT TOP 1 1 
                  FROM INFORMATION_SCHEMA.TABLES 
                 WHERE TABLE_NAME = 'Triagem' )
BEGIN
  CREATE TABLE Triagem(
  
  	IdTriagem              INT IDENTITY(1,1) NOT NULL CONSTRAINT PK_Triagem_IdTriagem PRIMARY KEY,
  	IdPaciente             INT               NOT NULL,
  	DataHoraAtendimento    DATETIME          NOT NULL,
  	[Status]               TINYINT           NOT NULL,
    ClassificacaoRisco     INT               NOT NULL CONSTRAINT DF_Triagem_ClassificacaoRisco DEFAULT(5),
  	IdEnfermeiro           INT               NULL,
  	DataHoraTermino        DATETIME          NULL,
  	Peso                   DECIMAL(5,2)      NULL,
  	Altura                 DECIMAL(3,2)      NULL,
  	PressaoArterial        VARCHAR(7)        NULL,
  	FrequenciaRespiratoria INT               NULL,
  	FrequenciaCardiaca     INT               NULL,
  	Temperatura            DECIMAL(3,1)      NULL,
  	Obersevacao            VARCHAR(MAX)      NULL,
  
  	CONSTRAINT FK_Triagem_IdPaciente
     FOREIGN KEY (IdPaciente)
  	REFERENCES Pessoa(IdPessoa),
  
  	CONSTRAINT FK_Triagem_IdEnfermeiro
     FOREIGN KEY (IdEnfermeiro)
  	REFERENCES Pessoa(IdPessoa),
  
  	CONSTRAINT CK_Triagem_Status
  	     CHECK ([Status] BETWEEN 1 AND 4),
  
  	CONSTRAINT CK_Triagem_ClassificacaoRisco
  	     CHECK (ClassificacaoRisco BETWEEN 1 AND 5),
  
  )
END 
GO

